                      <td bgcolor="#FFB200" class=" "><strong>HOW CAN I POST A PROJECT? </strong></td>
                    </tr>
                </table>
                 <span class="articleText"><br>To post a project, the imobilliare member has to be a registered Employer on imobilliare.<br>
                    The Employer logs on using his ID and Password, he then clicks on <strong>post a post project</strong> link which can be found around the imobilliare.com site. This link leads him to a page for posting projects. The Employer then gives details of the project and submits the project.<br>
                    The project posted can be viewed on the home page, projects viewage page and on other parts of the imobilliare.com site.<br>
                    Certain details must be filled and correctly before the project can be submitted.<br>
                    No fees are charged to the Employer for posting any project.<br>
                  When posting a project, ensure you specify explicitly details affecting the project under the Monetary details section. Details such as Project Budget, Preferred payment method, Extra features have been defined below:<br>
                  <span class="articleText"><br>
                  <span class="articleText">Project Budget:</span></span><span class="articleText"><br>
                  </span><span class="articleText">This provides viewers with how much the Employer is are willing to pay the selected developer to handle this project.
                  Give an realistic project budget as this could influence developers willing to bid for your project.<br>
                  </span><span class="articleText"><br>
                  <span class="articleText">Preferred Payment Method:</span><br>
                  </span><span class="articleText">This provides viewers with the means through which payment for the project will be made.</span><span class="articleText"><br>
                  <br>
                  <span class="articleText">Extra Features:</span></span><span class="articleText"><br>
                  <em>Bids Hidden:</em> Bids received for this project will be hidden from public view with only the Employer being able to view the bids.<br>
                  <br>
                  <em>imobilliare Should Choose Developer:</em> imobilliare Support Team will help the Employer select a developer from the bidders to handle the project.<br>
                  <br>
                  <em>Private:</em> Only listed developers will be able to bid for this project.</span><span class="articleText"> <br>

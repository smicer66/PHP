                      <td bgcolor="#FFB200" class=" "><strong>HOW DOES IMOBILLIARE WORK? </strong></td>
                    </tr>
                </table>
                  <span class="articleText">imobilliare.com serves as a linkage service between developers and employers.<br>
                  Two services are presently being offered by imobilliare.com. These include handling of software or computer-related projects.<br>
                  <br>
                  Towards this, a member on imobilliare.com can be classified as:<br>
                  <ul>
				  <li>a Developer, or </li>
                  <li>an Employer, or<br></li>
				  </ul>
                  Developers and Employers register on imobilliare.com in order to become a member.<br>
                  Employers posts projects on imobilliare.com which Developers can bid on. While the project is Open, messages could be sent between Developers and the Employer in order to persuade the Employer to assign the developer to a project.<br>
                  At a certain time, when the employer feels he has received the right bid and the right developer, he assigns the project to the developer.<br>
                  An email is then sent to the developer notifying him of his being chosen to handle the project. If he accepts to handle the project, the employer then pays a contact
                  fees before the contact details of both the employer and the developer are sent to their email addresses. </li>
                  </span>
                  
